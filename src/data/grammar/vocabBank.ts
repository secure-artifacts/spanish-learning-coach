export function pickVocabForUnit(level: CefrLevel, unitIndex: number, count = 8): VocabEntry[] {
  const pool = LEVEL_VOCAB[level];
  const result: VocabEntry[] = [];
  for (let i = 0; i < count; i++) {
    result.push(pool[(unitIndex * count + i) % pool.length]);
  }
  return result;
}

